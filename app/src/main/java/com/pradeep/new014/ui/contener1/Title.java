package com.pradeep.new014.ui.contener1;


public class Title  {
    private String Name;
    private String Pick;

    public Title() {
    }

    public Title(String name, String pick) {
        Name = name;
        Pick = pick;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getPick() {
        return Pick;
    }

    public void setPick(String pick) {
        Pick = pick;
    }
}
